    private void btnNetworkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNetworkActionPerformed

        String network = String.valueOf(txtNetwork.getText());

        if(network.isEmpty())
        {
            JOptionPane.showMessageDialog(btnNetwork, "Please enter a Network Name.");
        }
        else
        {
            business.getNetworkDirectory().createNetwork(network);
            txtNetwork.setText("");
            JOptionPane.showMessageDialog(btnNetwork, "Network added succesfully");
            PopulateTable();
        }
    }//GEN-LAST:event_btnNetworkActionPerformed

