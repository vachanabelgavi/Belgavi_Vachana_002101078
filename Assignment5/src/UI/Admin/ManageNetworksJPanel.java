    private void PopulateTable(){
        
        DefaultTableModel model = (DefaultTableModel) tableNetwork.getModel();
        model.setRowCount(0);
        for(Network n : business.getNetworkDirectory().getNetworkList()){
                
            Object[] row = new Object[1];
            row[0] = n.getName(); 
            ((DefaultTableModel) tableNetwork.getModel()).addRow(row);
        }
    }
