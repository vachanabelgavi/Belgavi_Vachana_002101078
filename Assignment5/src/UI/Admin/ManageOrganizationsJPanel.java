    private void populateCombo(){
        comboOrganization.removeAllItems();
        for (Organization.Type type : Organization.Type.values()){
            if (!type.getValue().equals(Organization.Type.Admin.getValue()))
                comboOrganization.addItem(type);
        }
    }
    
    private void populateNetworkComboBox()
    {
        for(Network n : business.getNetworkDirectory().getNetworkList())
        {
            comboNetwork.addItem(n);
        }
    }

    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tableOrganization.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : organizationDirectory.getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }
    
    
