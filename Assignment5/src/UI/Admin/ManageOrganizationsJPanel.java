    private void addOrgBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrgBtnActionPerformed

        try
        {
            Organization.Type type = (Organization.Type) comboOrganization.getSelectedItem();
            Organization org = organizationDirectory.createOrganization(type);
            Network network = (Network) comboNetwork.getSelectedItem();

            if(org instanceof CustomerOrganization)
            {
                network.setCustomerOrganization(org);
            }
            else if (org instanceof RestaurantOrganization)
            {
                network.setRestaurantOrganization(org);
            }
            else
            {
                network.setDeliverymanOrganization(org);
            }
            populateTable();
        }
        catch (NullPointerException e)
        {
            JOptionPane.showMessageDialog(addOrgBtn, "Please Add a Network.");
            return;
        }

    }//GEN-LAST:event_addOrgBtnActionPerformed


